	<h2>What is Hydraulic Fracturing, aka Fracking?</h2>
	<p>Fracking is a relatively new (since the early 2000's) mining process where a horizontal well shaft is bored deep underground, then injected with high pressure water, chemicals, and sand to force cracks open under ground to allow fossil fuels to return to the surface.&nbsp; See <a href="http://en.wikipedia.org/wiki/Hydraulic_fracturing" target="_blank">http://en.wikipedia.org/wiki/Hydraulic_fracturing</a> for more information.</p>

